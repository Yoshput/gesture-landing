/**
 * Get gesture by ID
 */
export function getGesture(id) {
    return GESTURE_DATA[id] || null;
}

/**
 * Get all gestures by category
 */
export function getGesturesByCategory(category) {
    return Object.entries(GESTURE_DATA)
        .filter(([_, gesture]) => gesture.category === category)
        .reduce((acc, [id, gesture]) => {
            acc[id] = gesture;
            return acc;
        }, {});
}

/**
 * Get all basic gestures
 */
export function getBasicGestures() {
    return getGesturesByCategory('basic');
}

/**
 * Get all alphabet gestures
 */
export function getAlphabetGestures() {
    return getGesturesByCategory('alphabet');
}

/**
 * Get all number gestures
 */
export function getNumberGestures() {
    return getGesturesByCategory('number');
}

/**
 * Get gestures by difficulty
 */
export function getGesturesByDifficulty(difficulty) {
    return Object.entries(GESTURE_DATA)
        .filter(([_, gesture]) => gesture.difficulty === difficulty)
        .reduce((acc, [id, gesture]) => {
            acc[id] = gesture;
            return acc;
        }, {});
}

/**
 * Get all gestures
 */
export function getAllGestures() {
    return GESTURE_DATA;
}

/**
 * Get gesture count by category
 */
export function getGestureCountByCategory() {
    return {
        basic: Object.values(GESTURE_DATA).filter(g => g.category === 'basic').length,
        alphabet: Object.values(GESTURE_DATA).filter(g => g.category === 'alphabet').length,
        number: Object.values(GESTURE_DATA).filter(g => g.category === 'number').length
    };
}

